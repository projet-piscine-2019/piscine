Les tarifs proposés comprennent les rabais et ristournes que la société ECE AMAZON serait amenée à octroyer compte tenu de ses résultats ou de la prise en charge par l'acheteur de certaines prestations.<br><br><br><br>

Clause n° 4 : Escompte
Aucun escompte ne sera consenti en cas de paiement anticipé. <br><br><br><br>
Clause n° 5 : Modalités de paiement

Le règlement des commandes s'effectue :
•soit par carte bancaire ;
•autres moyens de paiement acceptés.<br><br><br><br>


Clause n° 6 : Retard de paiement
En cas de défaut de paiement total ou partiel des marchandises livrées au jour de la réception, l'acheteur doit verser à la société ECE AMAZON une pénalité de retard égale à trois fois le taux de l'intérêt légal.
